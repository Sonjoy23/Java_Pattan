    public static void main(String[] args) {
        for(int i=1; i<=10; i++){
            for(int j=i; j<=10; j++){
                System.out.print("*");
            }
            for(int j=0+i; j<10; j++){
                System.out.print(" ");
            }
            System.out.println();
        }




    }
